# complex numbers ka symbol "complex" hi hota hai
# complex number = ( real numbers + imaginary numbers )
# real numbers = real numbers can be integer, float, zero or any valid numbers
# imaginary numbers = any real numbers adding with "j" char at last known as imaginary numbers
# example 

x1 = 12 + 13j
print("x1  ka value ", x1)
print("X1 ka type : ", type(x1))

x2 = 12.3 + 12j
print("x2  ka value ", x2)
print("X2 ka type : ", type(x2))

x3 = 0 + 23j
print("x3  ka value ", x3)
print("X3 ka type : ", type(x3))

# minus symbol will also work

y1 = 12 - 13j
print("y1  ka value ", y1)
print("y1 ka type : ", type(y1))

y2 = 12.3 - 12j
print("y2  ka value ", y2)
print("y2 ka type : ", type(y2))

y3 = 0 - 23j
print("y3  ka value ", y3)
print("y3 ka type : ", type(y3))

