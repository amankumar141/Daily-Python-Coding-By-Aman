# sets ka symbol "set" hota hai
# set = unordered collection of unique objects 
# denoted by symbols --> { Value1, Value2, .... } 
# examples : { 1, 2, 3, 4, 5 }

# same value will print if they are unique (koi same value repeat nahi hai to)
x1 = {1,2,3}
print("x1 ka value : ", x1)
print("x1 ka type: ", type(x1))

# only 1 value will print, agar aap same value bhut baar dete ho to
x2 = { 1, 2, 1, 3, 2, 4, 2, 5, 1, 6, 3, 7, 4, 7, 8, 3, 9, 4, 9, 5 } 
print("x2 ka value: ", x2)
print("x2 ka type: ", type(x2))

