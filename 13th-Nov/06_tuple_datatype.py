# tuples ka symbol "tuple" hota hai
# tuple =  ordered immutable sequence of objects 
# denoted by symbol --> ( Value1, Value2, ....)
# example: ( 1, 2, 3, 4, 5 )
# Note: agar ( ) miss kr jate hai aur direct likhte hai 1,2,3,4 to yeh bhi tuple hai

# with bracket 
x1= (1,2,3,4)
print("x1 ka value: ", x1)
print("x1 ka type: ", type(x1))

# without bracket 
x2= 1,2,3,4,5,6
print("x2 ka value: ", x2)
print("x2 ka type: ", type(x2))

